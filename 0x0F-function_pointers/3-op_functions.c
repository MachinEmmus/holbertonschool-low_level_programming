/**
 * op_add - Funtion to add
 * @a: first parametrer
 * @b: second parametrer
 * Return: Sum of two parametres
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Funtion to sub
 * @a: first parametrer
 * @b: second parametrer
 * Return: Sub of two parametres
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Funtion to mul
 * @a: first parametrer
 * @b: second parametrer
 * Return: mul of two parametres
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Funtion to div
 * @a: first parametrer
 * @b: second parametrer
 * Return: div of two parametres
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Funtion to mod
 * @a: first parametrer
 * @b: second parametrer
 * Return: mod of two parametres
 */
int op_mod(int a, int b)
{
	return (a % b);
}

